package Javaassignment_1;

public class Assignment_1 {

	public Assignment_1() {
	// A triangle three arms are: 5, 7 and 11 inches. Write a method to display the perimeter of triangle in console output. 
	}

	public static void main(String[] args) {
		
		int  a=5;                                      // Right arm a= 5
	    int  b=7;                                      // left  arm b= 7
		int  c=11;                                     // 3rd arm c=11
		int perimeter =a+b+c;                             //we know, perimeter of triangle is addition of all arms.
		
		System.out.print("the perimeter of triangle is "+perimeter);
		}
	

}
